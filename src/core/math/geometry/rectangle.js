/**
 * A Rectangle class
 */
export default class Rectangle {
  /**
   * @param Number x, y - the x and y values of the rectangle
   * @param Number width, height - the width and height values of the rectangle
   */
  constructor(x, y, width, height) {
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
  }
}
